		$this->floor_id->LookupFilters = array("dx1" => '`description`');
		ew_AddFilter($sWhereWrk, $sFilterWrk);
		$this->Lookup_Selecting($this->floor_id, $sWhereWrk); // Call Lookup Selecting
		if ($sWhereWrk <> "") $sSqlWrk .= " WHERE " . $sWhereWrk;
			$rswrk = Conn()->Execute($sSqlWrk);
			if ($rswrk && !$rswrk->EOF) { // Lookup values found
				$arwrk = array();
				$arwrk[1] = $rswrk->fields('DispFld');
				$this->floor_id->ViewValue = $this->floor_id->DisplayValue($arwrk);
				$rswrk->Close();
			} else {
				$this->floor_id->ViewValue = $this->floor_id->CurrentValue;
			}
		} else {
			$this->floor_id->ViewValue = NULL;
		}
		$this->floor_id->ViewCustomAttributes = "";

		// quantity
		$this->quantity->ViewValue = $this->quantity->CurrentValue;
		$this->quantity->ViewCustomAttributes = "";

			// description
			$this->description->LinkCustomAttributes = "";
			$this->description->HrefValue = "";
